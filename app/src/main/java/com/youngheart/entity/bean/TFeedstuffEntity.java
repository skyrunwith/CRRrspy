package com.youngheart.entity.bean;

public class TFeedstuffEntity {
    private String feedstuffid;
    private String name;
    private String size;
    private String company;

    public String getFeedstuffid() {
        return feedstuffid;
    }

    public void setFeedstuffid(String feedstuffid) {
        this.feedstuffid = feedstuffid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }
}
